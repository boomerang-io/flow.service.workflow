package io.boomerang.v4.model;

public class ActionSummary {

  private long approvalsRate;
  private long manual;
  private long approvals;
  public long getApprovalsRate() {
    return approvalsRate;
  }
  public void setApprovalsRate(long approvalsRate) {
    this.approvalsRate = approvalsRate;
  }
  public long getManual() {
    return manual;
  }
  public void setManual(long manual) {
    this.manual = manual;
  }
  public long getApprovals() {
    return approvals;
  }
  public void setApprovals(long approvals) {
    this.approvals = approvals;
  }
  

}
